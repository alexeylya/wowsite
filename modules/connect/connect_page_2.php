<div class="news-title"><br><b>Инструкция подключения к игре WoW</b><br><br></div>
<div class="mb_main">
Для игры на нашем сервере вам необходимо будет выполнить несколько шагов:<br><br>
<ul>
	<li>Получить клиент World of Warcraft - Wrath of The Lich King версии 3.3.5.12340<br>
		Скачать образ оригинального диска можно с <a href="http://rutracker.org/forum/viewtopic.php?t=1251626">rutracker.org</a><br>
		Патчи клиент скачивает и устанавливает автоматически, если вам не нравится этот метод то актуальный список ссылок на патчи можно найти на <a href="http://www.wowwiki.com/Patches">wowwiki.com</a><br>
	</li>
	<li>Зарегистрировать игровой аккаунт, приняв правила сервера<br>
		Зарегистрироваться можно здесь: <a href="/personal/?reg">Регистрация</a>
	</li>
	<li>Проверить содержимое файла <b>realmlist.wtf</b>, в нем должна быть следующее:<br><br>
		<code>
			<?php echo $site_realmlist;?>
		</code>
	</li><br>
</ul>
Realmlist.wtf располагается в вашем клиенте в папке: [диск]:\[путь]\World of Warcraft\Data\[локализация*]\<br><br>
*Для русских клиентов ruRU<br><br>
</div>
